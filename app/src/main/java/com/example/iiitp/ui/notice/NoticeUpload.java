package com.example.iiitp.ui.notice;

public class NoticeUpload {

    public String name;
    public String url;


    public NoticeUpload() {
    }

    public NoticeUpload(String name, String url) {
        this.name = name;
        this.url= url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

}
